#!/bin/bash
RES=`aws s3api head-bucket --bucket $1 2>&1`

echo ""
if [[ ${#RES} == 0 ]]
then
  echo "*******************************************************************************************"
  echo "Looks like you already have the bucket named $1. We suggest that you pick another name!"
  echo "*******************************************************************************************"
fi

if [[ $RES =~ "(403)" ]]
then
  echo "*******************************************************************************************"
  echo "Bucket name $1 is already taken, you will need to choose another bucket name :("
  echo "*******************************************************************************************"
fi

if [[ $RES =~ "(404)" ]]
then
  echo "*******************************************************************************************"
  echo "Bucket name $1 is available, You're good to go!"
  echo "*******************************************************************************************"
fi
echo ""
